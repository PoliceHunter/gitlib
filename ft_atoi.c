#include "libft.h"

int		ft_atoi(const char *str)
{
	long long	i;
	int			sig;
	int			j;

	j = 0;
	i = 0;
	sig = 1;
	while (*str == ' ' || *str == '\t' || *str == '\n'
			|| *str == '\r' || *str == '\v' || *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sig = -1;
		str++;
	}
	while (str[j] >= '0' && str[j] <= '9' && str[j] != '\0')
	{
		i = i * 10 + str[j++] - '0';
	}
	if ((j > 19 || i >= 9223372036854775807) && sig == 1)
		return (-1);
	else if ((j > 19 || i >= 9223372036854775807) && sig == -1)
		return (0);
	return (i * sig);
}
